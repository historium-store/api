import { Schema, model } from 'mongoose';

const { ObjectId } = Schema.Types;

const translatorSchema = new Schema(
	{
		fullName: {
			type: String,
			required: true
		},
		books: [
			{
				type: ObjectId,
				ref: 'Book',
				required: false
			}
		],
		createdAt: {
			type: Number
		},
		updatedAt: {
			type: Number
		}
	},
	{
		versionKey: false,
		timestamps: true
	}
);

const Translator = model('Translator', translatorSchema);

export default Translator;
